#pragma once

#include <chrono>

///////////////////////////////////////////////////////////////////////////////
/// \brief Simple class to perform time measurements
///////////////////////////////////////////////////////////////////////////////
class Timer
{
public:
	typedef std::chrono::system_clock::time_point Moment;

public:
	Timer();
	~Timer();

	///////////////////////////////////////////////////////////////////////////
	/// \brief Resets the timer
	///////////////////////////////////////////////////////////////////////////
	void reset();

	///////////////////////////////////////////////////////////////////////////
	/// \brief Returns the time elapsed since the last call to reset()
	///
	/// \return Elapsed time in seconds
	///////////////////////////////////////////////////////////////////////////
	double elapsedTime();

protected:
	Moment myStart;
};

